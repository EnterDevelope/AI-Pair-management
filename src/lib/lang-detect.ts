// 목적: 아이디어 텍스트의 언어를 유니코드 범위 정규식으로 감지한다.
//
// 감지 순서: 한글 → 가나(히라가나/카타카나) → CJK 한자 → en 기본값
// - 한글 우선(D-13): 한글+영문 혼합 입력에서 한국어로 감지
// - franc 패키지 불사용 결정 (공급망 게이트 검토 후 정규식만 사용)
// ============================================================

export type SupportedLang = "ko" | "en" | "ja" | "zh"

/**
 * 텍스트에서 지원 언어를 감지한다.
 *
 * @param text - 감지할 텍스트
 * @returns "ko" | "en" | "ja" | "zh"
 */
export function detectLang(text: string): SupportedLang {
  // 1. 한글 (U+AC00–U+D7A3 + 호환 자모 U+3130–U+318F)
  if (/[가-힣ㄱ-ㅎㅏ-ㅣ]/.test(text)) return "ko"

  // 2. 가나 - 히라가나(U+3041–U+309F) + 카타카나(U+30A0–U+30FF)
  if (/[ぁ-ゟ゠-ヿ]/.test(text)) return "ja"

  // 3. CJK 통합 한자 (U+4E00–U+9FFF)
  if (/[一-鿿]/.test(text)) return "zh"

  // 4. 기본값: 영어 (라틴 문자 및 기타 모든 문자)
  return "en"
}
